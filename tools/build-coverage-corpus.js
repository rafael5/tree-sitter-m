#!/usr/bin/env node
// Generates test/coverage/keywords.m — a synthetic M routine that
// exercises every (canonical, standard_status) pair in
// src/grammar-metadata.json so the per-tier coverage gate (criterion #5
// / spec §10.3) has at least one keyword node per pair to walk.
//
// Run after build-grammar.js if grammar-surface.json adds new keywords
// (the regen script chains it). Output is committed.
//
// The emitted source is parser-test fodder, not idiomatic M: most
// commands are argless, every function call uses a single zero arg.
// That's fine — the coverage gate only cares about keyword node
// recognition, not semantic validity.

'use strict';

const fs = require('fs');
const path = require('path');

const PROJECT_ROOT = path.resolve(__dirname, '..');
const METADATA_PATH = path.join(PROJECT_ROOT, 'src', 'grammar-metadata.json');
const OUT_PATH = path.join(PROJECT_ROOT, 'test', 'coverage', 'keywords.m');

// Operators that are unary-only (no LHS) — generated as ` <op>1`.
const UNARY_OPERATORS = new Set(['+', '-', "'"]);

function uniqueCanonicals(metadata, concept) {
  const seen = new Map();
  for (const k of Object.keys(metadata)) {
    if (!k.startsWith(`${concept}:`)) continue;
    for (const e of metadata[k]) {
      if (!seen.has(e.canonical)) seen.set(e.canonical, e.standard_status);
    }
  }
  return [...seen.keys()].sort();
}

function commandLine(canonical) {
  // Argless form. Every command in the grammar accepts a bare keyword
  // alone on a line as `command -> command_keyword`; argument lists are
  // optional in the rule. This holds for every canonical in metadata
  // (verified by the gate itself — if one ever stopped recognising,
  // the gate fails first).
  return ` ${canonical}`;
}

function functionLine(canonical) {
  // Functions are written as `WRITE $FN(0)` — one zero arg keeps the
  // call shape valid for argcount-permissive grammar AD-01.
  return ` W ${canonical}(0)`;
}

function specialVarLine(canonical) {
  return ` W ${canonical}`;
}

function operatorLine(op) {
  if (UNARY_OPERATORS.has(op)) return ` W ${op}1`;
  return ` W 1${op}1`;
}

function patternCodeLine(letter) {
  // Pattern match expression: `expr ? <count><code>`. Use `1<L>` per
  // letter so each pattern_letter node carries exactly that letter.
  return ` I 1?1${letter}`;
}

function main() {
  const md = JSON.parse(fs.readFileSync(METADATA_PATH, 'utf-8')).metadata;

  const lines = [];
  lines.push('COV ; tree-sitter-m coverage corpus — auto-generated by tools/build-coverage-corpus.js');
  lines.push(' ;');
  lines.push(' ; This routine is parser-fodder, not idiomatic M. It exists to');
  lines.push(' ; give the per-tier coverage gate at least one keyword node per');
  lines.push(' ; (canonical, standard_status) pair in src/grammar-metadata.json.');
  lines.push(' ; Do NOT hand-edit; rerun the build script if it falls out of sync.');

  lines.push(' ;');
  lines.push(' ; --- commands -----------------------------------------------------');
  for (const c of uniqueCanonicals(md, 'commands')) lines.push(commandLine(c));

  lines.push(' ;');
  lines.push(' ; --- intrinsic functions ------------------------------------------');
  for (const c of uniqueCanonicals(md, 'intrinsic_functions')) lines.push(functionLine(c));

  lines.push(' ;');
  lines.push(' ; --- intrinsic special variables ----------------------------------');
  for (const c of uniqueCanonicals(md, 'intrinsic_special_variables')) lines.push(specialVarLine(c));

  lines.push(' ;');
  lines.push(' ; --- operators ----------------------------------------------------');
  for (const c of uniqueCanonicals(md, 'operators')) lines.push(operatorLine(c));

  lines.push(' ;');
  lines.push(' ; --- pattern codes ------------------------------------------------');
  for (const c of uniqueCanonicals(md, 'pattern_codes')) lines.push(patternCodeLine(c));

  lines.push(' Q');
  lines.push('');

  fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
  fs.writeFileSync(OUT_PATH, lines.join('\n'));
  console.log(`wrote ${OUT_PATH} (${lines.length} lines)`);
}

main();
